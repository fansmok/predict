const BG_COLOR_SET = new Set(LEAGUE_BG_OPTIONS.map(o => o.color.toLowerCase()));

export function pickRandomLeagueEmoji(): string {
  const list = LEAGUE_EMOJI_OPTIONS as readonly string[];
  return list[Math.floor(Math.random() * list.length)] ?? DEFAULT_LEAGUE_EMOJI;
}

export function pickRandomLeagueBg(): string {
  const list = LEAGUE_BG_OPTIONS;
  return list[Math.floor(Math.random() * list.length)]?.color ?? DEFAULT_LEAGUE_BG;
}

export function pickRandomLeagueAppearance(): { emoji: string; bg: string } {
  return { emoji: pickRandomLeagueEmoji(), bg: pickRandomLeagueBg() };
}

export function normalizeLeagueBg(input: string | null | undefined): string {
  if (!input) return DEFAULT_LEAGUE_BG;
  const trimmed = input.trim().toLowerCase();
  if (BG_COLOR_SET.has(trimmed)) return trimmed;
  return DEFAULT_LEAGUE_BG;
}
