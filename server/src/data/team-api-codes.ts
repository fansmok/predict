export function resolveApiTeamId(apiTeam: {
  id?: number;
  name?: string;
  code?: string | null;
}): string | null {
  if (apiTeam.code) {
    const byCode = API_TEAM_CODE_MAP[apiTeam.code.toUpperCase()];
    if (byCode) return byCode;
  }
  if (apiTeam.name) {
    const byName = API_TEAM_NAME_MAP[apiTeam.name.trim()];
    if (byName) return byName;
  }
  return null;
}
